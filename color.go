package tcell

// Color represents a color.  Colors numeric values are taken from the XTerm
// 256 color map, except that they are offset by one, to allow 0 to indicate
// the default (unset) color.
type Color int16

const (
	ColorDefault Color = iota
	ColorBlack
	ColorRed
	ColorGreen
	ColorYellow
	ColorBlue
	ColorMagenta
	ColorCyan
	ColorWhite
	ColorGrey
	ColorBrightRed
	ColorBrightGreen
	ColorBrightYellow
	ColorBrightBlue
	ColorBrightMagenta
	ColorBrightCyan
	ColorBrightWhite
)
